using System;
using System.Collections.Generic;
using System.Data.Common;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Maze_Game
{
    public class MazeBuilder
    {
        private readonly int _row;
        private readonly int _column;
        private readonly string[,] _maze;
        private char[,] _finalForm;

        public MazeBuilder(int row, int column)
        {
            _row = row;
            _column = column;
            _maze = new string[,] {
             { "                                                 .==========================================================%=" },
            { "                                                 .:::::::::::::::::::::::::=@:::::::::::+@:::::::::::+@:::::%=" },
            { "                                                 .                         -@           +@           -@     %=" },
            { "                                                 .:::::::.    .:::::::.    -@     :.    =@     :.    -@     %=" },
            { "                                                 -@+++++@=    .++++++@+    :=     %+    :+     -@    -@     %=" },
            { "                                                 -@     %+           %+           %+           -@    -@     %=" },
            { "                                                 -@     %+           %+           %+           -@    -@     %=" },
            { "                                                 -@     *-    -@.    %%***********#************@=    :*     %=" },
            { "                                                 -@           -@     %+                        -@           %=" },
            { "                                                 -@           -@     %+                        -@           %=" },
            { "                                                 -@     +*****#@.    %+    :*     *************@%***********%=" },
            { "                                                 -@           -@     %+    -@     %+                 -@     %=" },
            { "                                                 -@           -@     %+    -@     %+                 -@     %=" },
            { "                                                 -@===========*@     %+    -@     %+    :=     =:    -@     %=" },
            { "                                                 -@::::::::::::-     :.    -@     %+    =@     -@    .:     %=" },
            { "                                                 -@                        -@     %+    =@     -@           %=" },
            { "                                                 -@     ..    .:     .:::::=@:::::@+    =@     %+:::::.     %=" },
            { "                                                 -@     %+    -@     =+++++*@++++++:    =@     %#+++++=     %=" },
            { "                                                 -@     %+    -@           -@           =@     -@           %=" },
            { "                                                 -@     %+    -@           -@           =@     -@           %=" },
            { "                                                 -@     %+    :%*****#******#     +*****#@     ******#%     %=" },
            { "                                                 -@     %+                              =@           -@     %=" },
            { "                                                 -@     %+                              =@           -@     %=" },
            { "                                                 -@     %%*************************-    =@******-    -@*****@=" },
            { "                                                 -@           -@                        =@     -@           %=" },
            { "                                                 -@           -@                        =@     -@           %=" },
            { "                                                 -@=====.     -@     -==================*@     %*======     %=" },
            { "                                                 -@:::::      -@     :::::::::::::::::::::     ::::::+@     %=" },
            { "                                                 -@           -@                                     -@       " },
            { "                                                 -@...........=@.....................................=@.....%=" },
            { "                                                 :++++++++++++++++++++++++++++++++++++++++++++++++++++++++++%=" }, };
            _finalForm = new char[_row, _column];
            Creating_final_form();
        }

        private void Creating_final_form()
        {
            for (int i = 0; i < _row; i++)
            {
                for (int j = 0; j < _column; j++)
                {
                    _finalForm[i, j] = _maze[i, 0][j];
                }
            }
        }

        public char[,] GenerateMazeLayout()
        {
            return _finalForm;
        }

    }
}
